package com.fix.obd.util.model;

public class CharacterIterator {
	private String cid;
	private String cname;
	private String clength;
	private String cdecode;
	private String cnotice;
	public String getCid() {
		return cid;
	}
	public void setCid(String cid) {
		this.cid = cid;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public String getClength() {
		return clength;
	}
	public void setClength(String clength) {
		this.clength = clength;
	}
	public String getCdecode() {
		return cdecode;
	}
	public void setCdecode(String cdecode) {
		this.cdecode = cdecode;
	}
	public String getCnotice() {
		return cnotice;
	}
	public void setCnotice(String cnotice) {
		this.cnotice = cnotice;
	}
}
