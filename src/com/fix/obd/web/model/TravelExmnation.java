package com.fix.obd.web.model;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name="travelexmnation")
public class TravelExmnation {
	@Id
	@GeneratedValue(strategy =GenerationType.IDENTITY)
	@Column(name="eid",nullable=false)
	private int eid;
	@Column(name="tid",nullable=false,columnDefinition="varchar(20)")
	private String terminalId;
	@Column(name="date",nullable=false,columnDefinition="varchar(19)")
	private String date;
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	@Column(name="total_distance",columnDefinition="varchar(20)")
	private String totalDistance;
	@Column(name="longest_distance",columnDefinition="varchar(20)")
	private String longestDistance;
	@Column(name="max_speed",columnDefinition="varchar(20)")
	private String maxSpeed;
	@Column(name="total_exceed_seconds",columnDefinition="varchar(20)")
	private String totalExceedSeconds;
	@Column(name="total_brake_times",columnDefinition="varchar(20)")
	private String totalBrakeTimes;
	@Column(name="total_emer_brake_times",columnDefinition="varchar(20)")
	private String totalEmerBrakeTimes;
	@Column(name="total_speed_up_times",columnDefinition="varchar(20)")
	private String totalSpeedUpTimes;
	@Column(name="total_emer_speed_up_times",columnDefinition="varchar(20)")
	private String totalEmerSpeedUpTimes;
	@Column(name="avg_speed",columnDefinition="varchar(20)")
	private String avgSpeed;
	public String getAvgSpeed() {
		return avgSpeed;
	}
	public void setAvgSpeed(String avgSpeed) {
		this.avgSpeed = avgSpeed;
	}
	public String getAvgOilExpend() {
		return avgOilExpend;
	}
	public void setAvgOilExpend(String avgOilExpend) {
		this.avgOilExpend = avgOilExpend;
	}
	@Column(name="max_water_tmp",columnDefinition="varchar(20)")
	private String maxWaterTmp;
	@Column(name="max_revolution",columnDefinition="varchar(20)")
	private String maxRevolution;
	@Column(name="total_oil_expend",columnDefinition="varchar(20)")
	private String totalOilExpend;
	@Column(name="avg_oil_expend",columnDefinition="varchar(20)")
	private String avgOilExpend;
	@Column(name="total_tired_driving_minutes",columnDefinition="varchar(20)")
	private String totalTiredDrivingMinutes;
	public int getEid() {
		return eid;
	}
	public void setEid(int eid) {
		this.eid = eid;
	}
	public String getTerminalId() {
		return terminalId;
	}
	public void setTerminalId(String terminalId) {
		this.terminalId = terminalId;
	}
	public String getTotalDistance() {
		return totalDistance;
	}
	public void setTotalDistance(String totalDistance) {
		this.totalDistance = totalDistance;
	}
	public String getLongestDistance() {
		return longestDistance;
	}
	public void setLongestDistance(String longestDistance) {
		this.longestDistance = longestDistance;
	}
	public String getMaxSpeed() {
		return maxSpeed;
	}
	public void setMaxSpeed(String maxSpeed) {
		this.maxSpeed = maxSpeed;
	}
	public String getTotalExceedSeconds() {
		return totalExceedSeconds;
	}
	public void setTotalExceedSeconds(String totalExceedSeconds) {
		this.totalExceedSeconds = totalExceedSeconds;
	}
	public String getTotalBrakeTimes() {
		return totalBrakeTimes;
	}
	public void setTotalBrakeTimes(String totalBrakeTimes) {
		this.totalBrakeTimes = totalBrakeTimes;
	}
	public String getTotalEmerBrakeTimes() {
		return totalEmerBrakeTimes;
	}
	public void setTotalEmerBrakeTimes(String totalEmerBrakeTimes) {
		this.totalEmerBrakeTimes = totalEmerBrakeTimes;
	}
	public String getTotalSpeedUpTimes() {
		return totalSpeedUpTimes;
	}
	public void setTotalSpeedUpTimes(String totalSpeedUpTimes) {
		this.totalSpeedUpTimes = totalSpeedUpTimes;
	}
	public String getTotalEmerSpeedUpTimes() {
		return totalEmerSpeedUpTimes;
	}
	public void setTotalEmerSpeedUpTimes(String totalEmerSpeedUpTimes) {
		this.totalEmerSpeedUpTimes = totalEmerSpeedUpTimes;
	}
	public String getMaxWaterTmp() {
		return maxWaterTmp;
	}
	public void setMaxWaterTmp(String maxWaterTmp) {
		this.maxWaterTmp = maxWaterTmp;
	}
	public String getMaxRevolution() {
		return maxRevolution;
	}
	public void setMaxRevolution(String maxRevolution) {
		this.maxRevolution = maxRevolution;
	}
	public String getTotalOilExpend() {
		return totalOilExpend;
	}
	public void setTotalOilExpend(String totalOilExpend) {
		this.totalOilExpend = totalOilExpend;
	}
	public String getTotalTiredDrivingMinutes() {
		return totalTiredDrivingMinutes;
	}
	public void setTotalTiredDrivingMinutes(String totalTiredDrivingMinutes) {
		this.totalTiredDrivingMinutes = totalTiredDrivingMinutes;
	}
}
